CREATE FUNCTION MY_PARAMS(FIRST_NAME VARCHAR(255),LAST_NAME VARCHAR(255))
RETURNS VARCHAR(255)
BEGIN
RETURN CONCAT(FIRST_NAME,' ',LAST_NAME);
END //
DELIMITER ;
