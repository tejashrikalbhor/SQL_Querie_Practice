-- ADDITION OF 2 NUM

DELIMITER //
CREATE FUNCTION ADDITION(NUM1 INT , NUM2 INT)
RETURNS INT
BEGIN
RETURN NUM1 + NUM2;
END //
DELIMITER ;
SELECT ADDITION(10,20);

-- CALCULATE AGE
DELIMITER //
CREATE FUNCTION CALCULATE_AGE(DOB DATE)
RETURNS INT
BEGIN
DECLARE TODAY_DATE DATE;
SELECT CURRENT_DATE() INTO TODAY_DATE;
RETURN YEAR(TODAY_DATE) - YEAR(DOB);
END //
DELIMITER ;

SELECT CALCULATE_AGE('1998-02-04') AS 'YOUR_AGE';
SHOW FUNCTION STATUS WHERE DB ='MY_SQL_QUERRY';
